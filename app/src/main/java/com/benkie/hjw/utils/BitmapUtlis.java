    /**
     * 将Bitmap转换成文件
     * 保存文件
     *
     * @param bm
     * @throws IOException
     */
    public String saveFile(Bitmap bm, String path) {
//        File dirFile = new File(path);
//        if(dirFile.exists()){
//            dirFile.delete();
//        }
        try {
            if (path.contains("/") & path.contains(".")) {
                int s = path.lastIndexOf("/");
                int end = path.lastIndexOf(".");
                String imgPath = path.substring(0, s);
                String name = path.substring(s, end);
                String imgType = path.substring(end, path.length());
                path = imgPath + name + "_condensed" + imgType;
            }
            File myCaptureFile = new File(path);
            if (myCaptureFile.exists()){
                myCaptureFile.delete();
            }
            BufferedOutputStream bos = new BufferedOutputStream(new FileOutputStream(myCaptureFile));
            bm.compress(Bitmap.CompressFormat.JPEG, 80, bos);
            bos.flush();
            bos.close();
            Log.d(getClass().getName(),"压缩后保存路径:"+path);
            int angle = readPictureDegree(path);
            Log.d(getClass().getName(),"回复旋转角度:"+angle);
        } catch (IOException e) {
            e.printStackTrace();
            Log.d(getClass().getName(),"压缩后保存路径:"+path);
        }
        return path;
    }




    /**
     * 读取照片旋转角度
     *
     * @param path 照片路径
     * @return 角度
     */
    public static int readPictureDegree(String path) {
        int degree = 0;
        try {
            ExifInterface exifInterface = new ExifInterface(path);
            int orientation = exifInterface.getAttributeInt(ExifInterface.TAG_ORIENTATION, ExifInterface.ORIENTATION_NORMAL);
            switch (orientation) {
                case ExifInterface.ORIENTATION_ROTATE_90:
                    degree = 90;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_180:
                    degree = 180;
                    break;
                case ExifInterface.ORIENTATION_ROTATE_270:
                    degree = 270;
                    break;
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return degree;
    }

    /**
     * 旋转图片
     * @param angle 被旋转角度
     * @param bitmap 图片对象
     * @return 旋转后的图片
     */
    public static Bitmap rotaingImageView(int angle, Bitmap bitmap) {
        Bitmap returnBm = null;
        // 根据旋转角度，生成旋转矩阵
        Matrix matrix = new Matrix();
        matrix.postRotate(angle);
        try {
            // 将原始图片按照旋转矩阵进行旋转，并得到新的图片
            returnBm = Bitmap.createBitmap(bitmap, 0, 0, bitmap.getWidth(), bitmap.getHeight(), matrix, true);
        } catch (OutOfMemoryError e) {
        }
        if (returnBm == null) {
            returnBm = bitmap;
        }
        if (bitmap != returnBm) {
            bitmap.recycle();
        }
        return returnBm;
    }

